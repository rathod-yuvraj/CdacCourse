// Accept Number from user and display table of that number in table format
function displayTable() {
    // Accept number from user
    let num = parseInt(document.getElementById("numberInput").value);   
    let tableBody = document.getElementById("tableBody");

    // Clear previous table data
    tableBody.innerHTML = "";
    // Generate table rows
    for (let i = 1; i <= 10; i++) {
        let row = document.createElement("tr");
        let operationCell = document.createElement("td");
        let resultCell = document.createElement("td");
        operationCell.textContent = `${num} x ${i}`;
        resultCell.textContent = num * i;
        row.appendChild(operationCell);
        row.appendChild(resultCell);
        tableBody.appendChild(row);
    }
    // Display the table
    document.getElementById("tableContainer").style.display = "block";
}
// Hide the table initially
document.getElementById("tableContainer").style.display = "none";
// Add event listener to the button
document.getElementById("displayButton").addEventListener("click", displayTable);
