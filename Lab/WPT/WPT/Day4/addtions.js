// write a function to add 3 numbers and print the values
function addThreeNumbers(a, b, c) {
    let sum = a + b + c;
    console.log("Sum of three numbers: " + sum);
}
addThreeNumbers(5, 10, 15);

// write a function to add 2 numbers and return the value
function addTwoNumbers(x, y) {
    return x + y;
}
let result = addTwoNumbers(20, 30);
console.log("Sum of two numbers: " + result);

// write a function to add 4 numbers and return the value
function addFourNumbers(p, q, r, s) {
    return p + q + r + s;
}
let total = addFourNumbers(1, 2, 3, 4);
console.log("Sum of four numbers: " + total);
